public class CultureAndSociety {
    String field;
    String achivements;

    public CultureAndSociety(String field, String achivements) {
        this.field = field;
        this.achivements = achivements;
    }
    public String getField() {
        return field;
    }

    public void setField(String field) {
        this.field = field;
    }

    public String getAchivements() {
        return achivements;
    }

    public void setAchivements(String achivements) {
        this.achivements = achivements;
    }
}
